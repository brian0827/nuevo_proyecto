git init: inicar repositorio
git clone 'url repo en git': clonar el repositorio localmente
git status: ver status de los archivos nuevos para subir al repositorio
git add . : agregar todos los archivos de la carpeta
git commit -m "detalle de que contiene el repo"
git remote add origin 'url_de_git_del_repo'
git push -u origin master : para subir codigo a la carpeta master
git rm nombre.archivo: eliminar un archivo del repo
git mv nombre_archivo ubicacion_final: mover un archivo del repo
git log: confirmaciones hechas en el repo
git log --stat: estadisticas de confirmaciones hechas en el repo
